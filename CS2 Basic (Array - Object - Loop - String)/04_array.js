// Array - index base [index start from 0]

let numbers = [10, 20, 30, 40, 50, 60];
console.log(numbers)

// Element access
console.log("Index 5 Value :", numbers[5])

// count elements (How many elements in array. To find size of an array.)
console.log("Length of the array is :", numbers)


// added last
numbers.push(100, 110)
console.log(numbers)

// remove last item
numbers.pop()
numbers.pop()
console.log(numbers)

// added first
numbers.unshift(500)
numbers.unshift(400)
console.log(numbers)

// remove from first
numbers.shift()
numbers.shift()
console.log(numbers)




